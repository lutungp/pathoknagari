                        <p style="text-align: justify;"><DT>Masjid Kagungan Dalem</DT></p>
                        <p style="text-align: justify; text-indent: 0.3in;">Pada zaman penjajahan Jepang yakni pada tahun 1940, Masjid Ad-Darojat dan masyarakat Babadan dipindah
                        ke Desa Babadan Jl. Kaliurang, Kentungan, Sleman. Perpindahan ini dikarenakan saat itu daerah Babadan terkena pelebaran pangkalan pesawat terbang dan sebagai
                        gudang senjata. Akibat perpindahan tersebut denyut kampung Babadan sebagai kampung santri sempat mengalami tidur panjang. Akibat perpindahan
                        yang dilakukan oleh Jepang tersebut, masjid Patok Negara atau Negoro tersebut menjadi tak terurus.Saat terjadi pengusiran oleh Jepang,
                        memang tidak semua penduduk ikut boyong ke Kentungan. Sebagian warga Babadan tetap tinggal di kampung halamannya.
                        Setelah ditinggalkan warga, masjid ini hanya tersisa fondasi dan temboknya saja. Hal ini dikarenakan seluruh
                        konstruksi kayu masjid ikut dipindah dan dibangun kembali di Babadan Kentungan. Setelah Jepang kalah dalam Perang Dunia
                        ke-2 yang akhirnya seluruh personil dan tentaranya meninggalkan Indonesia, secara otomatis pembangunan perluasan pangkalan udara pun urung dilaksanakan.
                        Sekitar tahun 1950-an mulai banyak masyarakat yang datang ke kampung Babadan dan akhirnya menetap di sana.</p>
                        <p style="text-align: justify;">Pada tahun 1960-an salah seorang warga Babadan bernama Muthohar membangun kembali masjid peninggalan Sultan Hamengkubuwono I tersebut. Pembangunan kembali masjid tersebut dilakukan semasa Sri Sultan Hamengku Buwono IX. Atas dukungan Sultan maka nama Sultan Hamengku Buwana IX "Ndoro Jatun" diabadikan menjadi nama masjid Patok Negara tersebut dengan nama Masjid Ad-Darojat. Meski bentuk masjid mengalami perubahan, namun bentuk khas sebagai masjid kraton masih tetap dipertahankan. Seperti pada masjid Pathok Negoro lainnya, di sisi barat masjid adalah pemakaman tempat bersemayam para tokoh agama maupun masyarakat setempat. Karena latar belakang sejarah demikian ini, antara warga Babadan dengan Babadan Baru Kentungan meskipun terpisah secara geografis namun tetap terjalin hubungan yang harmonis. Setiap tahun menjelang datangnya bulan suci Ramadhan, banyak warga Babadan Baru yang datang ke Babadan untuk menggelar acara tradisi nyadran. Silaturahmi setiap kegiatan nyadran tersebut berlanjut saat Lebaran Idul Fitri tiba, karena banyak juga diantara mereka yang masih merupakan saudara sedarah.</p>
                        <p style="text-align: justify;"><DT>Tata Arsitektur</DT></p>
                        <p style="text-align: justify; text-indent: 0.3in;">﻿﻿Pertama kali masjid ini dibangun pada tahun 1774, arsitektur Masjid Ad-Darojat sama persis dengan ketiga masjid Patok Negara lainnya. Kesamaan bentuk masjid tersebut terlihat hampir di semua bagian. Bangunan ruang utama masjid menggunakan konstruksi joglo dengan empat soko guru ﻿﻿﻿dan terdapat pawestren disampingnya. ﻿ ﻿Serambi masjid menggunakan konstruksi bentuk limasan serta terdapat kolam di sebelah timur masjid sebagai tempat bersuci sebelum memasuki masjid, di depan masjid juga terdapat pohon kepel. Dikarenakan pengusiran oleh Jepang pada tahun 1940-an, bersamaan dengan boyongnya penduduk Babadan ke Kentungan, seluruh bangunan masjid ikut dipindah dan dibangun kembali di daerah Kentungan. Tempat tersebut kemudian diberi nama Kampung Babadan Baru. Baru pada tahun 1960-an bekas lokasi masjid di Babadan kembali dibangun .</p>
                        <p style="text-align: justify;">Pada pembangunan awal di tahun 1964, bentuk masjid masih semi permanen. Baru pada tahun 1988 dibangun kembali serambi tengah dengan sumber dana dari pemerintah dan swadaya masyarakat. Meski bentuk masjid mengalami perubahan, namun ciri khas sebagai Masjid Pathok Negara tetap dipertahankan, seperti mustoko masjid yang masih disimpan dengan baik. Baru pada tahun 1992 bangunan induk utama dibongkar kembali dan disarankan agar disesuaikan seperti bentuk semula yakni joglo yang berasal dari kayu jati.</p>
                        <p style="text-align: justify;">Pada tahun 1993 akhirnya pembangunan ruang utama masjid berhasil dilakukan dengan membangun joglo dengan 4 soko guru masing-masing setinggi 7 meter. Pembangunan kelengkapan masjid seperti serambi depan, gerbang masuk, serta tempat wudhu dan wc dilakukan pada tahun 2001. Atas kesepakatan para tokoh agama setempat pada tahun 2003, mustoko yang asli yang terbuat dari tanah liat tidak jadi dipasang dan diganti dengan mustoko dari kuningan. Meskipun demikian mustoko yang asli sampai sekarang masih tersimpan dengan baik di Masjid Ad-Darojat.</p>
                        <p style="text-align: justify;">Melalui peranan masjid ini, masyarakat Babadan begitu lekat dengan ajaran-ajaran Islam. Di tengah-tengah masyarakat pada akhirnya memang muncul beragam pandangan. Namun keragaman ini dapat disikapi dengan bijak oleh warga masyarakat Babadan. Toleransi di kampung santri Babadan sungguh dapat menjadi teladan dalam kehidupan beragama di masyarakat. Terlebih dalam menyikapi adanya perbedaan pandangan di kalangan masyarakat, perbedaan pandangan di kalangan umat Islam dalam menjalani syariat, tidak berlkaku bagi masyarakt perkampungan santri Babadan. Rasa toleransi ini telah terjalin sejak lama terutama dalm menghadapi bulan Ramadhan yakni pada pelaksanaan sholat tarawih. Fanatisme perbedaan faham antara NU dan Muhammadiyah dapat diantisipasi dengan baik oleh masyarakat Babadan.</p>
